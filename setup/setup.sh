#!/bin/bash
# Terradev Account Setup Automation Script
# Sets up all necessary accounts and API keys

set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Configuration
ENV_FILE=".env"
SETUP_LOG="setup.log"

# Initialize log
echo "Terradev Account Setup Log - $(date)" > $SETUP_LOG

# Function to print colored output
print_status() {
    echo -e "${GREEN}[INFO]${NC} $1" | tee -a $SETUP_LOG
}

print_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1" | tee -a $SETUP_LOG
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1" | tee -a $SETUP_LOG
}

print_header() {
    echo -e "${BLUE}$1${NC}" | tee -a $SETUP_LOG
}

# Function to prompt for API key
prompt_api_key() {
    local key_name=$1
    local description=$2
    local required=$3
    
    print_header "ðŸ”§ Setting up $description"
    
    if [ "$required" = "true" ]; then
        while true; do
            echo -n "Enter your $key_name: "
            read -s api_key
            echo
            if [ -n "$api_key" ]; then
                echo "$key_name=$api_key" >> $ENV_FILE
                print_status "$key_name configured"
                break
            else
                print_error "$key_name is required"
            fi
        done
    else
        echo -n "Enter your $key_name (optional, press Enter to skip): "
        read -s api_key
        echo
        if [ -n "$api_key" ]; then
            echo "$key_name=$api_key" >> $ENV_FILE
            print_status "$key_name configured"
        else
            print_warning "$key_name skipped"
        fi
    fi
}

# Function to prompt for file path
prompt_file_path() {
    local key_name=$1
    local description=$2
    local default_path=$3
    
    print_header "ðŸ”§ Setting up $description"
    
    echo -n "Enter path to $key_name (default: $default_path): "
    read file_path
    
    if [ -z "$file_path" ]; then
        file_path="$default_path"
    fi
    
    echo "$key_name=$file_path" >> $ENV_FILE
    print_status "$key_name set to: $file_path"
}

# Function to open browser for signup
open_signup_page() {
    local url=$1
    local service_name=$2
    
    print_header "ðŸŒ Opening signup page for $service_name"
    
    if command -v open > /dev/null; then
        open "$url"
    elif command -v xdg-open > /dev/null; then
        xdg-open "$url"
    else
        print_warning "Please manually open: $url"
    fi
    
    echo "Please complete the signup and return here to enter your API key."
    echo "Press Enter when you have your API key..."
    read
}

# Function to check if command exists
command_exists() {
    command -v "$1" >/dev/null 2>&1
}

# Function to validate API key format
validate_api_key() {
    local key=$1
    local min_length=$2
    
    if [ ${#key} -lt $min_length ]; then
        print_error "API key seems too short (minimum $min_length characters)"
        return 1
    fi
    
    return 0
}

